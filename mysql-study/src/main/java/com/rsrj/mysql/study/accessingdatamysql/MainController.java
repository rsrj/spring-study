package com.rsrj.mysql.study.accessingdatamysql;

import java.util.Optional;

import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Controller;
import org.springframework.web.bind.annotation.DeleteMapping;
import org.springframework.web.bind.annotation.GetMapping;
import org.springframework.web.bind.annotation.PostMapping;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestParam;
import org.springframework.web.bind.annotation.ResponseBody;

@Controller //This means that this class is a Controller
@RequestMapping(path="/demo") //This means URL's start with/demo(after Application path)
public class MainController {
	@Autowired  //This means to get the bean called userRepository
				// Which is auto-generated by Spring we will use it to handle the data
	private UserRepository userRepository;
	
	@PostMapping(path="/add")
	public @ResponseBody String addNewUser (@RequestParam String name, @RequestParam String email) {
		//@ResponseBody means the returned String is the response, not a view name
		//@RequestParam means it is a parameter from GET or POST request
		
		User n = new User();
		n.setName(name);
		n.setEmail(email);
		userRepository.save(n);
		return "Saved";
	}
	
	@GetMapping(path = "/all")
	public @ResponseBody Iterable<User> getAllUsers(){
		//This returns a JSON or XML with the users
		return userRepository.findAll();
	}
	
	@GetMapping(path = "/findbyid")
	public @ResponseBody Optional<User> getUser(@RequestParam int id) {
		return userRepository.findById(id);
	}
	
	@DeleteMapping(path = "/deletebyid")
	public @ResponseBody String removeUserById(@RequestParam int id) {
		try {
			userRepository.deleteById(id);
			return "Successfully removed";
		} catch (IllegalArgumentException e) {
			return e.getMessage().toString();
		}
		
	}
	
}
